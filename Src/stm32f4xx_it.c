/**
* @brief This function handles TIM1 update interrupt and TIM10 global interrupt.
*/
void TIM1_UP_TIM10_IRQHandler(void)
{
  /* USER CODE BEGIN TIM1_UP_TIM10_IRQn 0 */
	static uint32_t cnt_int=0;//кол-во прерываний в холостую
  /* USER CODE END TIM1_UP_TIM10_IRQn 0 */
  HAL_TIM_IRQHandler(&htim1);
  /* USER CODE BEGIN TIM1_UP_TIM10_IRQn 1 */
	Nec._cnt =0xff-DMA2_Stream6->NDTR;//кол-во принятых импульсов и пауз
	HAL_GPIO_TogglePin(GPIOD, LED1_Pin);
	if (cnt_int==5)//если возникло случайное прерывание
	{
		SET_BIT(TIM1->DIER, TIM_DIER_CC1IE);
		CLEAR_BIT(TIM1->DIER, TIM_DIER_UIE);		
		cnt_int=0;
	}
	if (Nec._cnt>=4)
	{
		SET_BIT(TIM1->DIER, TIM_DIER_CC1IE);
		CLEAR_BIT(TIM1->DIER, TIM_DIER_UIE);
		HAL_GPIO_TogglePin(GPIOD, LED2_Pin);
		tim_icc_dma_reload(DMA2_Stream6, 0, Nec._len_pulse_raw, 255);
		cnt_int=0;
		NEC_decode_callback(&Nec);
	}	
	else cnt_int++;
  /* USER CODE END TIM1_UP_TIM10_IRQn 1 */
}

